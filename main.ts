sprites.onOverlap(SpriteKind.Player, SpriteKind.potionMaterial, function (sprite, otherSprite) {
    otherSprite.destroy()
    countMaterials += 1
    if (level == 0) {
        game.splash("You collected", "Fresh Water!")
    } else if (level == 1) {
        game.splash("You collected ", "Otherworldly Branch!")
    } else if (level == 2) {
        game.splash("You collected ", "Funny Mushroom!")
    } else if (level == 3) {
        game.splash("You collected ", "Large Seashell!")
    }
    if (countMaterials == 4) {
        game.splash("You have collected", "all materials!")
        if (level == 2) {
            game.splash("Go brew your potion!")
        } else {
            game.splash("Go to the Village", "to brew your potion!")
        }
    }
    if (countCountMaterial == 0) {
        game.splash("Materials Left: " + (4 - countMaterials))
        countCountMaterial += 1
    } else if (countCountMaterial > 0) {
        countCountMaterial += -1
    }
})
